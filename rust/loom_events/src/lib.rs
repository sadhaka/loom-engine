//! loom_events - tamper-evident, HMAC-SHA256-chained event log (Rust core).
//!
//! The native sibling of the engine's TS `EventChain` (src/runtime/event-chain.ts)
//! and the server-authoritative event tape that guards TheWorldTable's combat +
//! currency ledger. Every record is signed with HMAC-SHA256 and folds in the
//! PREVIOUS record's signature, so the whole log is a hash chain: field tampering,
//! deletion, reordering, and (with a seal) tail truncation all break verification.
//!
//! BYTE-COMPATIBLE WITH THE TS CHAIN. The TS source carries a "not promised
//! byte-compatible with other languages" disclaimer - but its canonical message
//! format is in fact fully deterministic, and this crate reproduces it EXACTLY so
//! a record signed here verifies in TS and vice-versa. The three cross-language
//! traps and how each is defeated:
//!
//!   1. `field(s) = s.length + ':' + s` uses the JS UTF-16 string length, NOT the
//!      UTF-8 byte length - so `field()` here counts `s.encode_utf16().count()`.
//!   2. The canonical string form is JS `JSON.stringify(s)` - hand-written below
//!      (`js_json_string`) to match its exact escape set, NOT serde_json's.
//!   3. Object keys are sorted by JS `Array.sort()` order (UTF-16 code units),
//!      reproduced by sorting on `encode_utf16()` - not Rust's default scalar Ord
//!      (the two differ only for astral-plane keys, which event data never uses,
//!      but we match anyway).
//!
//! The shared golden vectors (test_vectors/event_chain_v1.json) are GENERATED by
//! running the real TS, then asserted by both the TS and the Rust test harness -
//! a real parity proof, not a hand-copied constant.
//!
//! FLOAT BOUNDARY (honest scope): a signed payload's numbers must be integers.
//! Integral floats (`7.0`) canonicalize as integers (matching JS `String(7.0) ===
//! "7"`); a real fraction is rejected fail-closed, because matching V8's
//! shortest-float dtoa is out of scope AND the determinism rule bans fractions
//! from signed event data. The numeric core (loom_math) is integer-only for the
//! same reason.

use hmac::{Hmac, Mac};
use serde_json::Value;
use sha2::Sha256;

type HmacSha256 = Hmac<Sha256>;

// Domain tags - record sigs and seal sigs live in separate namespaces so a
// record HMAC can never be reinterpreted as a seal HMAC. Trailing /1 is a format
// version. MUST match the TS constants verbatim.
const RECORD_DOMAIN: &str = "loom.chain.rec/1";
const SEAL_DOMAIN: &str = "loom.chain.seal/1";

// Hard cap on canonicalization recursion - matches the TS MAX_CANONICAL_DEPTH so
// a hostile deeply-nested payload is rejected at the same boundary on both sides.
const MAX_CANONICAL_DEPTH: usize = 256;

/// A payload the chain refuses to sign. serde_json::Value cannot hold the
/// NaN/Infinity/symbol/Date/function shapes the TS rejects, so those rejections
/// are simply unrepresentable here; only these two remain.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum CanonError {
    /// A number with a real fractional part (see the FLOAT BOUNDARY note).
    NonIntegerNumber,
    /// An integer outside JS's safe range [-(2^53-1), 2^53-1]. TS canonicalizes
    /// via JSON.parse, which silently rounds beyond 2^53 (9007199254740993 ->
    /// ...992), so signing such a value here would diverge from TS. Reject to
    /// keep the accepted numeric surface JS-safe-integer-only (Codex P1).
    UnsafeInteger,
    /// Negative zero (-0.0). TS rejects it fail-closed; we match (a signed 0 must
    /// not be mutable to -0 and still verify).
    NegativeZero,
    /// An own "__proto__" data key. TS rejects it (a JSON-parsed __proto__ cannot
    /// round-trip through a normal-prototype clone); we match for parity.
    ForbiddenKey,
    /// Payload nesting exceeds MAX_CANONICAL_DEPTH.
    DepthExceeded,
}

// ---------------------------------------------------------------------------
// Primitives
// ---------------------------------------------------------------------------

/// HMAC-SHA256 -> 64-char lowercase hex. The algorithm is FIPS/RFC-standardized,
/// so this is byte-identical to the engine's hand-rolled TS hmacSha256Hex for the
/// same (key, message) bytes (proven by the RFC 4231 test vector below).
pub fn hmac_sha256_hex(key: &[u8], message: &str) -> String {
    hmac_sha256_hex_bytes(key, message.as_bytes())
}

/// HMAC-SHA256 over RAW message bytes (may contain interior NUL / non-UTF-8) ->
/// 64-char lowercase hex. The &str form above is just this with message.as_bytes().
pub fn hmac_sha256_hex_bytes(key: &[u8], message: &[u8]) -> String {
    let mut mac =
        <HmacSha256 as Mac>::new_from_slice(key).expect("HMAC accepts a key of any length");
    mac.update(message);
    let out = mac.finalize().into_bytes();
    let mut hex = String::with_capacity(64);
    for b in out.iter() {
        hex.push_str(&format!("{:02x}", b));
    }
    hex
}

/// JS String.length = UTF-16 code-unit count (NOT bytes, NOT scalar count).
fn utf16_len(s: &str) -> usize {
    s.encode_utf16().count()
}

/// Length-prefixed field `<utf16len>:<value>` - self-delimiting, so concatenating
/// fields is injective (a value cannot forge a boundary). Matches TS `field()`.
fn field(s: &str) -> String {
    let mut out = String::with_capacity(s.len() + 8);
    out.push_str(&utf16_len(s).to_string());
    out.push(':');
    out.push_str(s);
    out
}

/// Exactly JS `JSON.stringify(s)` for a string (including the surrounding quotes):
/// escape `"` `\` and the C0 control set (\b \t \n \f \r, else \u00xx lowercase);
/// every other code point - including all non-ASCII and DEL - is emitted literally.
/// Rust &str is always valid UTF-8, so there are no lone surrogates to reject.
fn js_json_string(s: &str) -> String {
    let mut out = String::with_capacity(s.len() + 2);
    out.push('"');
    for c in s.chars() {
        match c {
            '"' => out.push_str("\\\""),
            '\\' => out.push_str("\\\\"),
            '\u{0008}' => out.push_str("\\b"),
            '\u{0009}' => out.push_str("\\t"),
            '\u{000A}' => out.push_str("\\n"),
            '\u{000C}' => out.push_str("\\f"),
            '\u{000D}' => out.push_str("\\r"),
            c if (c as u32) < 0x20 => out.push_str(&format!("\\u{:04x}", c as u32)),
            c => out.push(c),
        }
    }
    out.push('"');
    out
}

/// JS Number.MAX_SAFE_INTEGER (2^53 - 1). Beyond this JS integers are not exact,
/// so the signed numeric surface is restricted to [-MAX_SAFE_INT, MAX_SAFE_INT].
const MAX_SAFE_INT: i64 = 9_007_199_254_740_991;

#[inline]
fn safe_int(i: i64) -> Result<String, CanonError> {
    if (-MAX_SAFE_INT..=MAX_SAFE_INT).contains(&i) {
        Ok(i.to_string())
    } else {
        Err(CanonError::UnsafeInteger)
    }
}

/// JS `String(n)` for a JSON number, restricted to JS-SAFE integers (Codex P1:
/// TS canonicalizes through JSON.parse, which rounds past 2^53). See FLOAT BOUNDARY.
#[allow(clippy::float_cmp)]
fn canonical_number(n: &serde_json::Number) -> Result<String, CanonError> {
    if let Some(i) = n.as_i64() {
        return safe_int(i);
    }
    if let Some(u) = n.as_u64() {
        // u64 above i64::MAX is automatically > MAX_SAFE_INT -> rejected.
        return if u <= MAX_SAFE_INT as u64 {
            Ok(u.to_string())
        } else {
            Err(CanonError::UnsafeInteger)
        };
    }
    if let Some(f) = n.as_f64() {
        if f.is_finite() {
            // Codex P0: reject -0.0 to match the TS surface (Object.is(v, -0)).
            if f == 0.0 && f.is_sign_negative() {
                return Err(CanonError::NegativeZero);
            }
            // Round-trip through an integer cast (no float arithmetic): if the
            // value survives, it is an exact integer and stringifies like one,
            // matching JS where 7.0 and 7 are the same number -> "7". The
            // safe-integer bound then matches TS Number.isSafeInteger.
            let i = f as i64;
            if i as f64 == f {
                return safe_int(i);
            }
            if f >= 0.0 {
                let u = f as u64;
                if u as f64 == f {
                    return if u <= MAX_SAFE_INT as u64 {
                        Ok(u.to_string())
                    } else {
                        Err(CanonError::UnsafeInteger)
                    };
                }
            }
        }
    }
    Err(CanonError::NonIntegerNumber)
}

/// Deterministic STRICT JSON: object keys sorted (UTF-16 order), no whitespace,
/// fail-closed on a non-integer number. Mirrors TS `canonicalJson`.
fn canonical_json(value: &Value, depth: usize) -> Result<String, CanonError> {
    if depth > MAX_CANONICAL_DEPTH {
        return Err(CanonError::DepthExceeded);
    }
    match value {
        Value::Null => Ok("null".to_string()),
        Value::Bool(b) => Ok(if *b { "true" } else { "false" }.to_string()),
        Value::String(s) => Ok(js_json_string(s)),
        Value::Number(n) => canonical_number(n),
        Value::Array(arr) => {
            let mut items: Vec<String> = Vec::with_capacity(arr.len());
            for v in arr {
                items.push(canonical_json(v, depth + 1)?);
            }
            Ok(format!("[{}]", items.join(",")))
        }
        Value::Object(map) => {
            // Codex P0: reject an own "__proto__" data key to match TS.
            if map.contains_key("__proto__") {
                return Err(CanonError::ForbiddenKey);
            }
            // Sort by UTF-16 code-unit sequence to match JS Object.keys().sort().
            let mut keys: Vec<&String> = map.keys().collect();
            keys.sort_by(|a, b| a.encode_utf16().cmp(b.encode_utf16()));
            let mut pairs: Vec<String> = Vec::with_capacity(keys.len());
            for k in keys {
                let mut pair = js_json_string(k);
                pair.push(':');
                pair.push_str(&canonical_json(&map[k], depth + 1)?);
                pairs.push(pair);
            }
            Ok(format!("{{{}}}", pairs.join(",")))
        }
    }
}

/// The exact injective string fed to HMAC for one record. Matches TS
/// `canonicalMessage`: domain + seq + type + canonical(payload) + prevSig, each
/// length-prefixed.
fn canonical_message(
    seq: u64,
    type_: &str,
    payload: &Value,
    prev_sig: &str,
) -> Result<String, CanonError> {
    let mut msg = String::new();
    msg.push_str(&field(RECORD_DOMAIN));
    msg.push_str(&field(&seq.to_string()));
    msg.push_str(&field(type_));
    msg.push_str(&field(&canonical_json(payload, 0)?));
    msg.push_str(&field(prev_sig));
    Ok(msg)
}

fn seal_message(count: u64, head: &str) -> String {
    let mut m = String::new();
    m.push_str(&field(SEAL_DOMAIN));
    m.push_str(&field(&count.to_string()));
    m.push_str(&field(head));
    m
}

/// The HMAC hex signature for one record, byte-identical to the TS per-record
/// `sig`. Errors only if the payload is non-canonicalizable (see CanonError).
/// This is the parity-critical primitive the golden vectors lock down.
pub fn sign_record(
    key: &[u8],
    seq: u64,
    type_: &str,
    payload: &Value,
    prev_sig: &str,
) -> Result<String, CanonError> {
    Ok(hmac_sha256_hex(key, &canonical_message(seq, type_, payload, prev_sig)?))
}

/// Constant-time hex/byte equality - length is not secret (early-return), the
/// rest is xor-accumulated so no early exit leaks a match prefix. Mirrors TS
/// `timingSafeEqualHex`.
fn constant_time_eq(a: &[u8], b: &[u8]) -> bool {
    if a.len() != b.len() {
        return false;
    }
    let mut diff: u8 = 0;
    for i in 0..a.len() {
        diff |= a[i] ^ b[i];
    }
    diff == 0
}

