/// One signed record. Field names mirror the TS ChainedRecord (`type` is `type_`
/// here because `type` is a Rust keyword).
#[derive(Debug, Clone)]
pub struct ChainedRecord {
    pub seq: u64,
    pub type_: String,
    pub payload: Value,
    pub prev_sig: String,
    pub sig: String,
}

/// A signed (count, head) commitment - persist out of band to later detect tail
/// truncation. Mirrors TS ChainSeal.
#[derive(Debug, Clone)]
pub struct ChainSeal {
    pub count: u64,
    pub head: String,
    pub sig: String,
}

/// Tamper-evident HMAC-chained event log. `create -> append* -> verify / seal`.
pub struct EventChain {
    records: Vec<ChainedRecord>,
    key: Vec<u8>,
    genesis: String,
    head_sig: String,
    next_seq: u64,
}

impl EventChain {
    /// New empty chain. `genesis` is the root prevSig for the first record (binds
    /// the chain to a context, e.g. a world seed); pass "" for the default.
    pub fn create(key: &[u8], genesis: &str) -> Self {
        EventChain {
            records: Vec::new(),
            key: key.to_vec(),
            genesis: genesis.to_string(),
            head_sig: genesis.to_string(),
            next_seq: 1,
        }
    }

    /// Append + sign a record. Returns the stored record, or None on rejection
    /// (empty type / non-canonicalizable payload) WITHOUT advancing the sequence -
    /// fail-closed, exactly like the TS append.
    pub fn append(&mut self, type_: &str, payload: Value) -> Option<&ChainedRecord> {
        if type_.is_empty() {
            return None;
        }
        let seq = self.next_seq;
        let prev_sig = self.head_sig.clone();
        let sig = match sign_record(&self.key, seq, type_, &payload, &prev_sig) {
            Ok(s) => s,
            Err(_) => return None,
        };
        self.records.push(ChainedRecord {
            seq,
            type_: type_.to_string(),
            payload,
            prev_sig,
            sig: sig.clone(),
        });
        self.next_seq = seq + 1;
        self.head_sig = sig;
        self.records.last()
    }

    /// Current head signature - the value the NEXT append folds in. Equals the
    /// genesis anchor when empty.
    pub fn head(&self) -> &str {
        &self.head_sig
    }

    pub fn size(&self) -> usize {
        self.records.len()
    }

    pub fn records(&self) -> &[ChainedRecord] {
        &self.records
    }

    /// Recompute every signature AND verify chain linkage. true iff intact.
    pub fn verify(&self) -> bool {
        Self::verify_records(&self.key, &self.records, &self.genesis)
    }

    /// Verify an external record set (e.g. loaded from disk) without an instance.
    pub fn verify_records(key: &[u8], records: &[ChainedRecord], genesis: &str) -> bool {
        let mut prev_actual = genesis.to_string();
        for rec in records {
            let expected = match sign_record(key, rec.seq, &rec.type_, &rec.payload, &rec.prev_sig)
            {
                Ok(s) => s,
                Err(_) => return false, // stored record no longer canonicalizable
            };
            if !constant_time_eq(expected.as_bytes(), rec.sig.as_bytes()) {
                return false;
            }
            if rec.prev_sig != prev_actual {
                return false; // deletion / reordering broke the link
            }
            prev_actual = rec.sig.clone();
        }
        true
    }

    /// Sign the current (count, head) so a holder can later prove no records were
    /// dropped off the END. Persist out of band.
    pub fn seal(&self) -> ChainSeal {
        let count = self.records.len() as u64;
        let head = self.head_sig.clone();
        let sig = hmac_sha256_hex(&self.key, &seal_message(count, &head));
        ChainSeal { count, head, sig }
    }

    /// Verify a seal's own signature (constant-time).
    pub fn verify_seal(key: &[u8], seal: &ChainSeal) -> bool {
        let expected = hmac_sha256_hex(key, &seal_message(seal.count, &seal.head));
        constant_time_eq(expected.as_bytes(), seal.sig.as_bytes())
    }
}

#[cfg(test)]
mod tests {
    use super::*;
    use serde_json::json;

    #[test]
    fn hmac_matches_rfc4231_case2() {
        // RFC 4231 Test Case 2 - proves the HMAC-SHA256 primitive is correct, so
        // any byte-match with TS reduces purely to the canonical-message bytes.
        let got = hmac_sha256_hex(b"Jefe", "what do ya want for nothing?");
        assert_eq!(
            got,
            "5bdcc146bf60754e6a042426089575c75a003f089d2739839dec58b964ec3843"
        );
    }

    #[test]
    fn canonical_json_sorts_keys_and_escapes() {
        // Keys sorted; string escaped; integer (not 7.0) stringified plainly.
        let v = json!({ "b": "x\ty", "a": 7, "c": [1, 2] });
        assert_eq!(
            canonical_json(&v, 0).unwrap(),
            "{\"a\":7,\"b\":\"x\\ty\",\"c\":[1,2]}"
        );
    }

    #[test]
    fn integral_float_stringifies_as_integer() {
        assert_eq!(canonical_json(&json!(7.0), 0).unwrap(), "7");
        // A real fraction is rejected fail-closed.
        assert_eq!(canonical_json(&json!(7.5), 0), Err(CanonError::NonIntegerNumber));
    }

    #[test]
    fn rejects_ts_forbidden_shapes() {
        // Codex P0 parity: -0.0 and an own __proto__ key are rejected, matching TS.
        assert_eq!(
            canonical_json(&json!(-0.0), 0),
            Err(CanonError::NegativeZero)
        );
        let proto = serde_json::from_str::<Value>("{\"__proto__\":{\"x\":1}}").unwrap();
        assert_eq!(canonical_json(&proto, 0), Err(CanonError::ForbiddenKey));
        // sign_record surfaces the rejection (no signature for a forbidden payload).
        assert!(sign_record(b"k", 1, "t", &json!(-0.0), "").is_err());
    }

    #[test]
    fn rejects_unsafe_js_integers() {
        // Codex P1: integers beyond JS's safe range would round in TS's JSON.parse,
        // so signing them here would diverge. 2^53-1 is the last safe value.
        assert_eq!(canonical_json(&json!(9007199254740991_i64), 0).unwrap(), "9007199254740991");
        assert_eq!(canonical_json(&json!(-9007199254740991_i64), 0).unwrap(), "-9007199254740991");
        for &bad in &[9007199254740992_i64, 9007199254740993_i64, -9007199254740992_i64] {
            assert_eq!(
                canonical_json(&json!(bad), 0),
                Err(CanonError::UnsafeInteger),
                "{} must be rejected as JS-unsafe",
                bad
            );
        }
    }

    #[test]
    fn field_counts_utf16_units_not_bytes() {
        // An astral char is 1 scalar / 4 UTF-8 bytes / 2 UTF-16 units (JS .length).
        assert_eq!(field("\u{1F600}"), "2:\u{1F600}");
        // A BMP non-ASCII char is 1 UTF-16 unit.
        assert_eq!(field("\u{00E9}"), "1:\u{00E9}"); // e-acute
    }

    #[test]
    fn chain_appends_verifies_and_detects_tamper() {
        let mut chain = EventChain::create(b"runtime-secret", "");
        chain.append("combat.hit", json!({ "target": "goblin", "dmg": 7 }));
        chain.append("xp.award", json!({ "amount": 500 }));
        assert!(chain.verify());
        assert_eq!(chain.size(), 2);

        // Tamper a stored payload - verify must fail.
        let mut tampered: Vec<ChainedRecord> = chain.records().to_vec();
        tampered[0].payload = json!({ "target": "goblin", "dmg": 9 });
        assert!(!EventChain::verify_records(b"runtime-secret", &tampered, ""));

        // Delete the middle (here: the first) record - broken link.
        let truncated: Vec<ChainedRecord> = chain.records()[1..].to_vec();
        assert!(!EventChain::verify_records(b"runtime-secret", &truncated, ""));
    }

    #[test]
    fn seal_round_trips() {
        let mut chain = EventChain::create(b"k", "genesis-seed");
        chain.append("e", json!({ "n": 1 }));
        let seal = chain.seal();
        assert!(EventChain::verify_seal(b"k", &seal));
        assert_eq!(seal.count, 1);
    }
}
